package com.project.curve.core.type;

/**
 * 이벤트 중요도 레벨.
 * <p>
 * 이벤트의 중요도에 따라 처리 우선순위를 결정하거나 알림을 발송하는 데 사용됩니다.
 */
public enum EventSeverity {
    /**
     * 일반적인 정보성 이벤트 (기본값).
     * 비즈니스 로직의 정상적인 흐름을 나타냅니다.
     */
    INFO,

    /**
     * 경고성 이벤트.
     * 시스템에 문제는 없으나 주의가 필요한 상황입니다.
     */
    WARN,

    /**
     * 오류 이벤트.
     * 비즈니스 로직 처리 중 실패가 발생했음을 나타냅니다.
     */
    ERROR,

    /**
     * 치명적인 오류 이벤트.
     * 시스템의 주요 기능이 마비되거나 즉각적인 조치가 필요한 상황입니다.
     */
    CRITICAL
}
